function basGetAjaxList(url)
{
	$.ajax({
		url: url,
		success: function(res) {

			$('#bas_ajax_block').html(res);

			history.replaceState(null, '', $('#basSeCurPage').val());

			$('h1').text($('#basCurTitle').val());
			$('title').text($('#basCurMetaTitle').val());
			$('meta[name="description"]').attr('content',$('#basCurMetaDescription').val());
			$('#header-breadcrumb').html($('#basCurBreadcrumbs').html());

			$('.count_info b').text($('#basCoutProduct').val());

			var badges = JSON.parse($('#basCurFilterBadges').val());
			$('.catalog-filter-selected').html('');

			if(badges.length > 0)
				$('.catalog-filter-selected-wrap').show();
			else
				$('.catalog-filter-selected-wrap').hide();

			for(var i = 0; i < badges.length; i++)
			{
				$('.catalog-filter-selected').append('<a class="filter-badge" href="#" data-control="' + badges[i].CONTROL_ID + '">' + badges[i].NAME + ' <span class="filter-badge-close"></span></a>');
			}

			$(".overload-cat").hide();
			$(".overload-cat-img").hide();
		}
	});
}

var bas_timer = null;

function basGetAjax()
{
	if (!!bas_timer) clearTimeout(bas_timer);

	bas_timer = setTimeout(function(){

        $(".overload-cat").show();
        $(".overload-cat-img").show();

        $.ajax({
            url: $('.smartfilter').attr('action'),
            data: $('.smartfilter').serialize(),
			dataType: "json",
            success: function(res) {

            	if($('.bx_filter_code__ST_MAN').find('input:checked').length > 0)
            		$('.bx_filter_code__ST_COLLECTION').show();
            	else
            		$('.bx_filter_code__ST_COLLECTION').hide();

				for (var key in res.DISABLED)
				{
					if (res.DISABLED[key])
					{
						$('[data-role="label_' + key + '"]').addClass('disabled');
						$('#' + key + '-styler').addClass('disabled');
						$('#' + key + '').prop('disabled', true);
						if($('[data-role="label_' + key + '"]').data('collection') == 1)
							$('[data-role="label_' + key + '"]').hide();
					}
					else
					{
						$('[data-role="label_' + key + '"]').removeClass('disabled');
						$('#' + key + '-styler').removeClass('disabled');
						$('#' + key + '').prop('disabled', false);
						if($('[data-role="label_' + key + '"]').data('collection') == 1)
							$('[data-role="label_' + key + '"]').show();
					}
				}

                basGetAjaxList(res.URL);
            }
        });

	}, 500);
}

